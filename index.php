<?php

$quotes = [
    "Делай сегодня то, что другие не хотят, завтра будешь жить так, как другие не могут.",
    "Ошибка – это возможность начать снова, но уже более мудро.",
    "Только тот, кто рискует идти далеко, узнает, насколько далеко он может зайти.",
    "Не бойся ошибаться. Бойся повторять одну и ту же ошибку.",
    "Будущее зависит от того, что ты делаешь сегодня."
];

// Выбираем случайную цитату
$randomQuote = $quotes[array_rand($quotes)];

// Логируем в файл
